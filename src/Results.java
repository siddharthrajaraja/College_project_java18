                String lname=rs.getString(5);
                double sem1=rs.getDouble(6);
                double sem2=rs.getDouble(7);
                double avg=rs.getDouble(8);
                
                if (RB1.isSelected()==true)
                    model.addRow(new Object[]{srno,roll,fname,mname,lname,sem1,sem2,avg});
                if (RB2.isSelected()==true)
                    model1.addRow(new Object[]{srno,roll,fname,mname,lname,sem1,sem2,avg});
                if (RB3.isSelected()==true)
                    model2.addRow(new Object[]{srno,roll,fname,mname,lname,sem1,sem2,avg});
                if (RB4.isSelected()==true)
                    model3.addRow(new Object[]{srno,roll,fname,mname,lname,sem1,sem2,avg});
                
                
                
                
                
                
